    private void AccountSelectionBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AccountSelectionBoxActionPerformed
        String accountType = (AccountSelectionBox.getSelectedItem()).toString();
        AccountType = accountType;
        for (Branch branch : bank.getBranches()) {
            if (BranchSelectionBox.getSelectedItem().equals(branch.getName())) {
                if (AccountType.equals("Checkings")) {
                    AccountBalanceField.setText(Double.toString(branch.getUsers().get(currentUser).checking.getBalance()));
                    WithdrawalButton.setEnabled(true);
                    DepositButton.setEnabled(true);
                    AmountField.setEnabled(true);
                }
                if (AccountType.equals("Savings")) {
                    AccountBalanceField.setText(Double.toString(branch.getUsers().get(currentUser).saving.getBalance()));
                    WithdrawalButton.setEnabled(true);
                    DepositButton.setEnabled(true);
                    AmountField.setEnabled(true);
                }
                if (AccountType.equals("Select")) {
                    AccountBalanceField.setText(Double.toString(0));
                    WithdrawalButton.setEnabled(false);
                    DepositButton.setEnabled(false);
                    AmountField.setEnabled(false);
                    AmountField.setText("");
                }
            }
        }

    }//GEN-LAST:event_AccountSelectionBoxActionPerformed

    private void SignInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignInButtonActionPerformed
        String username = "";
        String password = "";
        String ConfirmPassword = "";
        String ssn = "";
        StringBuilder warnings = new StringBuilder();
        if (UserNameField.getText().isEmpty()) {
            warnings.append("Username must not be empty.\n");
        } else {
            username = UserNameField.getText();
        }
        //Verify last name field
        if (PasswordField.getText().isEmpty()) {
            warnings.append("Last name must not be empty.\n");
        } else {
            password = PasswordField.getText();
        }
        if (!SSNField.getText().matches("\\d{3}-?\\d{2}-?\\d{4}")) {
            warnings.append("SSN must be 9 digits, dashes are acceptable.\n");
        } else {
            ssn = SSNField.getText().replace("-", "");
        }
        //Verify initial deposit
        if (ConfirmPasswordField.getText().isEmpty() || !ConfirmPasswordField.getText().equals(PasswordField.getText())) {
            warnings.append("Passwords Must Match");
        } else {
            ConfirmPassword = ConfirmPasswordField.getText();
        }
        if (warnings.length() > 0) {
            JOptionPane.showMessageDialog(this, warnings.toString(), "Input Warnings", JOptionPane.WARNING_MESSAGE);
        } else {
            //Toronto.getUsers().get(currentUser);
            boolean signedIn = false;
            for (Branch branch : bank.getBranches()) {
                if (BranchSelectionBox.getSelectedItem().equals(branch.getName())) {
                    currentUser = branch.compare(username, ssn, password);
                    signedIn = currentUser != -1;
                }
                if (signedIn) {
                    AccountSelectionBox.setSelectedIndex(0);
                    AccountSelectionBox.setEnabled(true);
                    AccountBalanceField.setText("");
                    AccountBalanceField.setEnabled(true);
                    AmountField.setText("");
                    AmountField.setEnabled(true);
                } else {
                    AccountSelectionBox.setSelectedIndex(0);
                    AccountSelectionBox.setEnabled(false);
                    AccountBalanceField.setText("");
                    AccountBalanceField.setEnabled(false);
                    AmountField.setText("");
                    AmountField.setEnabled(false);
                }
            }
        }

    }//GEN-LAST:event_SignInButtonActionPerformed

