    private void WithdrawalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_WithdrawalButtonActionPerformed
        double amount = Double.parseDouble(AmountField.getText());
        for (Branch branch : bank.getBranches()) {
            if (BranchSelectionBox.getSelectedItem().equals(branch.getName())) {
                if (AccountType.equals("Checkings")) {
                    branch.getUsers().get(currentUser).checking.withdrawal(amount);
                    AccountBalanceField.setText(Double.toString(branch.getUsers().get(currentUser).checking.getBalance()));
                }
                if (AccountType.equals("Savings")) {
                    branch.getUsers().get(currentUser).saving.withdrawal(amount);
                    AccountBalanceField.setText(Double.toString(branch.getUsers().get(currentUser).saving.getBalance()));
                }
            }
        }
    }//GEN-LAST:event_WithdrawalButtonActionPerformed

